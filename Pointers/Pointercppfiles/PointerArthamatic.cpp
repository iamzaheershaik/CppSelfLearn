    p++;
    cout<<*p<<endl;
    cout<<"___________________________________________"<<endl;
    p--;
    cout<<*p<<endl;
    cout<<"___________________________________________"<<endl;
    cout<<p<<endl;
    cout<<p+1<<endl;
    cout<<"___________________________________________"<<endl;
    cout<<*p<<endl;
    cout<<*(p+3)<<endl;
    cout<<"___________________________________________"<<endl;
    cout<<"___________________________________________"<<endl;
    for (int i=0; i<5; i++) {
        cout<<A[i]<<endl;
    }
    cout<<"___________________________________________"<<endl;
    for (int i=0; i<5; i++) {
        cout<<i[A]<<endl;
    }
    cout<<"___________________________________________"<<endl;
